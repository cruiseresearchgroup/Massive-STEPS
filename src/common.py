def load_entity_data(entity_file, country=None, continent=None):
    entity_df = pd.read_csv(entity_file)
    entity_df["continent"] = entity_df["country"].apply(lambda x: COUNTRY_CONTINENTS.get(x, None))

    if continent is not None:  # world_place.csv
        entity_df = entity_df[entity_df["continent"] == continent]

    if country is not None:  # world_place.csv
        entity_df["country"] = entity_df["country"].apply(lambda x: COUNTRY_NORM.get(x, None))
        entity_df = entity_df[entity_df["country"].notnull()]
        entity_df = entity_df[entity_df["country"] == country]

    return entity_df
